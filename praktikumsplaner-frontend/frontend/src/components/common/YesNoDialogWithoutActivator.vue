<template>
    <v-dialog
        v-model="visible"
        persistent
        width="800"
    >
        <v-card>
            <v-card-title>
                {{ props.dialogtitle }}
            </v-card-title>
            <v-card-text
                v-for="(text, index) in props.dialogtext.split('\n')"
                :key="index"
            >
                {{ text }}
            </v-card-text>
            <v-card-actions>
                <v-spacer />
                <v-btn
                    id="yesnodialog-btn-no"
                    variant="text"
                    @click="no"
                >
                    Nein
                </v-btn>
                <v-btn
                    id="yesnodialog-btn-yes"
                    color="primary"
                    @click="yes"
                >
                    Ja
                </v-btn>
            </v-card-actions>
        </v-card>
    </v-dialog>
</template>
<script setup lang="ts">
import { computed } from "vue";

const props = defineProps<{
    dialogtitle: string;
    dialogtext: string;
    /**
     * Steuerflag fÃ¼r den Dialog
     */
    modelValue: boolean;
}>();

const emits = defineEmits<{
    (e: "no"): void;
    (e: "yes"): void;
    (e: "update:modelValue", v: boolean): void;
}>();

const visible = computed({
    get: () => props.modelValue,
    set: (v) => emits("update:modelValue", v),
});

function no(): void {
    emits("no");
}
function yes(): void {
    emits("yes");
}
</script>

